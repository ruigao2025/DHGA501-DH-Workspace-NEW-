        for word, flag in words:
            if flag == 'nr' and 2 <= len(word) <= 4:
                # éªŒè¯æ˜¯å¦åŒ…å«å¸¸è§å§“æ°
                if any(word.startswith(surname) for surname in self.surname_set):
                    persons.add(word)

        return sorted(list(persons))

